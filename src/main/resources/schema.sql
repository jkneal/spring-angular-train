CREATE TABLE PRODUCT_T (
  ID IDENTITY PRIMARY KEY,
  NAME VARCHAR(120)
);

CREATE TABLE STORE_T (
  NAME VARCHAR(120),
  OWNER VARCHAR(40),
  OPEN BOOLEAN,
  PRIMARY KEY (NAME, OWNER)
);

CREATE TABLE ORDER_T (
  ID BIGINT PRIMARY KEY,
  PRODUCT_ID INT
);

